const calculate = {};

calculate.sqrt = function sqrt(first) {
  return Math.sqrt(first);
};

calculate.addition = function addition(first, second) {
  return first + second;
};

calculate.substract = function substract(first, second) {
  return first - second;
};

calculate.div = function div(first, second) {
  return first / second;
};

calculate.multi = function multi(first, second) {
  return first * second;
};
